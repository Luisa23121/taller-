//Cree un programa que tome la base y la altura de un triángulo e imprima su área.//

alert("Bienvendios a la calculadora del area de un triangulo")

let base = parseInt(prompt("Ingrese el valor de la base: "))
let altura = parseInt(prompt("Ingrese el valor de la altura: "))
let area = (base * altura) / 2 

alert(`el area del triangulo es; ${area}`)

