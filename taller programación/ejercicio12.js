/*Cree un programa que realice la conversión de grados Celsius a Fahrenheit*/

//°F = °C × (9/5) + 32 .//

alert("Bienvenido a la calculadora de grados")

let Celsius = parseInt(prompt("Ingrese los grados celsius"))
let Fahrenheit = (Celsius * 9/5) + 32

alert(`Los grados fahrenheit son: ${Fahrenheit}`)
